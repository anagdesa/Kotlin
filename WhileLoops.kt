/*
while merupakan perulangan yang sangat flesible, 
dimana dilakukan cek kondisi,
ketika kondisi tru maka blok while dan teru diulang hingga bernilai false

 */

 fun main() {
     var i = 0

     while( i < 10){
         println("Tampilkan perulangan ke $i")
         i++
     }
    println("Perulangan Selesai")
 }